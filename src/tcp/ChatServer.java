    private void listenSocket(){
        try{
            gate=new ServerSocket(PORT);
            System.out.println("waiting!!!");
            socket=gate.accept();
            System.out.println("successfull!!");
        }catch(IOException e){
            System.out.println(e);
        }
        try{
            out=new DataOutputStream(socket.getOutputStream());
            in=new DataInputStream(socket.getInputStream());
            String ms=null;
            while(true){
                ms=in.readUTF();
                jTextArea1.append("from student: "+ms+"\n");
            }
        }catch(IOException e){
            System.out.println(e);
        }
    }
    protected void finalize(){
        try{
            in.close();
            out.close();
            socket.close();
            gate.close();
        }catch(IOException e){
            System.out.println(e);
        }
        
        
    }
